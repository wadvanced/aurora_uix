defmodule Mix.Tasks.Auix.Gen.TailwindClasses do
  @shortdoc "Generates a Tailwind CSS safelist for Aurora UIX icon classes"

  @moduledoc """
  Scans Aurora UIX source files for hero icon class names and writes a minimal
  JavaScript safelist to `priv/static/classes.js`.

  Host applications using Heroicons can reference this file in their Tailwind
  CSS configuration instead of scanning the entire Aurora UIX dependency tree:

      @source "../deps/aurora_uix/priv/static/classes.js";

  This task is idempotent: it only writes the file when the content changes.
  """

  use Mix.Task

  @output_file "priv/static/classes.js"
  @source_dir "lib"
  @hero_pattern ~r/hero-[a-z0-9]+(?:-[a-z0-9]+)*/

  @doc """
  Scans `lib/` for hero icon class names and writes the safelist file.
  """
  @spec run(keyword()) :: :ok
  def run(_) do
    classes =
      @source_dir
      |> collect_source_files()
      |> Enum.flat_map(&scan_file/1)
      |> Enum.uniq()
      |> Enum.sort()

    content = build_js(classes)

    if content == existing_content() do
      IO.puts("\e[32m#{@output_file} is already up-to-date\e[0m")
    else
      File.write!(@output_file, content)
      IO.puts("\e[32mUpdated #{@output_file} with #{length(classes)} icon classes\e[0m")
    end
  end

  ## PRIVATE

  @spec collect_source_files(binary()) :: [binary()]
  defp collect_source_files(dir) do
    dir
    |> File.ls!()
    |> Enum.flat_map(&expand_path(Path.join(dir, &1)))
  end

  @spec expand_path(binary()) :: [binary()]
  defp expand_path(path) do
    cond do
      File.dir?(path) -> collect_source_files(path)
      String.ends_with?(path, ".ex") or String.ends_with?(path, ".heex") -> [path]
      true -> []
    end
  end

  @spec scan_file(binary()) :: [binary()]
  defp scan_file(path) do
    path
    |> File.read!()
    |> then(&Regex.scan(@hero_pattern, &1))
    |> List.flatten()
  end

  @spec build_js([binary()]) :: binary()
  defp build_js(classes) do
    """
    /* Generated by mix auix.gen.tailwind_classes. Do not edit. */
    export default "#{Enum.join(classes, " ")}"
    """
  end

  @spec existing_content() :: binary()
  defp existing_content do
    case File.read(@output_file) do
      {:ok, content} -> content
      {:error, _} -> ""
    end
  end
end
