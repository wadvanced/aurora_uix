defmodule Mix.Tasks.Auix.Gen.StylesheetTest do
  use ExUnit.Case, async: false

  alias Mix.Tasks.Auix.Gen.Stylesheet

  @css_dir "assets/css"
  @variables_file Path.join(@css_dir, "auix-variables.css")
  @rules_file Path.join(@css_dir, "auix-rules.css")
  @combined_file Path.join(@css_dir, "auix-stylesheet.css")
  @custom_file Path.join(@css_dir, "auix-custom.css")
  @baseline_file Path.join(@css_dir, "auix-baseline.css")

  setup context do
    tmp_dir = Map.fetch!(context, :tmp_dir)
    original_dir = File.cwd!()
    File.cd!(tmp_dir)

    on_exit(fn -> File.cd!(original_dir) end)

    {:ok, tmp_dir: tmp_dir}
  end

  @tag :tmp_dir
  test "default invocation does not create auix-custom.css", _context do
    Stylesheet.run([])

    assert File.exists?(@variables_file)
    assert File.exists?(@rules_file)
    assert File.exists?(@combined_file)
    refute File.exists?(@custom_file)
  end

  @tag :tmp_dir
  test "default invocation does not create auix-baseline.css", _context do
    Stylesheet.run([])

    assert File.exists?(@variables_file)
    assert File.exists?(@rules_file)
    assert File.exists?(@combined_file)
    refute File.exists?(@baseline_file)
  end

  @tag :tmp_dir
  test "--baseline first run creates auix-baseline.css with expected structure", _context do
    Stylesheet.run(["--baseline"])

    assert File.exists?(@baseline_file)

    content = File.read!(@baseline_file)

    assert content =~ "/* Generated by auix.gen.stylesheet."
    assert content =~ "@layer auix.baseline {"
    assert content =~ "html, :host {"
  end

  @tag :tmp_dir
  test "--baseline is idempotent without --force", _context do
    File.mkdir_p!(@css_dir)
    sentinel = "/* sentinel content - must not be overwritten */\n"
    File.write!(@baseline_file, sentinel)

    Stylesheet.run(["--baseline"])

    assert File.read!(@baseline_file) == sentinel
  end

  @tag :tmp_dir
  test "--baseline --force overwrites existing auix-baseline.css", _context do
    File.mkdir_p!(@css_dir)
    sentinel = "/* sentinel content */\n"
    File.write!(@baseline_file, sentinel)

    Stylesheet.run(["--baseline", "--force"])

    content = File.read!(@baseline_file)
    refute content == sentinel
    assert content =~ "/* Generated by auix.gen.stylesheet."
  end

  @tag :tmp_dir
  test "--baseline and --custom are independent", context do
    tmp_dir = context.tmp_dir

    File.cd!(tmp_dir)
    Stylesheet.run(["--baseline"])
    assert File.exists?(@baseline_file)
    refute File.exists?(@custom_file)

    File.rm!(@baseline_file)
    File.cd!(tmp_dir)
    Stylesheet.run(["--custom"])
    assert File.exists?(@custom_file)
    refute File.exists?(@baseline_file)

    File.rm!(@custom_file)
    File.cd!(tmp_dir)
    Stylesheet.run(["--baseline", "--custom"])
    assert File.exists?(@baseline_file)
    assert File.exists?(@custom_file)
  end

  @tag :tmp_dir
  test "--custom first run creates auix-custom.css with expected structure", _context do
    Stylesheet.run(["--custom"])

    assert File.exists?(@variables_file)
    assert File.exists?(@rules_file)
    assert File.exists?(@custom_file)

    content = File.read!(@custom_file)

    assert content =~ "@layer auix.bridge {"
    assert content =~ ":root, :host {"
    assert content =~ "/* ----- Sizes & dimensions ----- */"
    assert content =~ "/* ----- Typography ----- */"
    assert content =~ "/* ----- Opacity ----- */"
    assert content =~ "/* ----- Shadows & rings ----- */"
    assert content =~ "/* ----- Colors ----- */"
    assert content =~ "/* --auix-border-radius-default:"
  end

  @tag :tmp_dir
  test "--custom is idempotent without --force", _context do
    File.mkdir_p!(@css_dir)
    sentinel = "/* sentinel content - must not be overwritten */\n"
    File.write!(@custom_file, sentinel)

    Stylesheet.run(["--custom"])

    assert File.read!(@custom_file) == sentinel
  end

  @tag :tmp_dir
  test "--custom --force overwrites existing auix-custom.css", _context do
    File.mkdir_p!(@css_dir)
    sentinel = "/* sentinel content */\n"
    File.write!(@custom_file, sentinel)

    Stylesheet.run(["--custom", "--force"])

    content = File.read!(@custom_file)
    refute content == sentinel
    assert content =~ "Generated by mix auix.gen.stylesheet"
  end

  @tag :tmp_dir
  test "combined shim does not import auix-custom.css", _context do
    Stylesheet.run(["--custom"])

    shim = File.read!(@combined_file)
    refute shim =~ "auix-custom.css"
    assert shim =~ "auix-variables.css"
    assert shim =~ "auix-rules.css"
  end
end
